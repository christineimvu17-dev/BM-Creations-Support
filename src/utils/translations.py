def get_text(key: str, language: str = "en", **kwargs) -> str:
    lang_dict = TRANSLATIONS.get(language, TRANSLATIONS["en"])
    text = lang_dict.get(key, TRANSLATIONS["en"].get(key, key))
    if kwargs:
        try:
            text = text.format(**kwargs)
        except (KeyError, ValueError):
            pass
    return text
